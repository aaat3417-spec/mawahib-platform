const LanguageContext = createContext(null);

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem("mawahib_language") || "en");
  const isArabic = language === "ar";

  useEffect(() => {
    localStorage.setItem("mawahib_language", language);
    document.documentElement.lang = language;
    document.documentElement.dir = isArabic ? "rtl" : "ltr";
  }, [isArabic, language]);

  const value = useMemo(() => ({
    language,
    isArabic,
    dir: isArabic ? "rtl" : "ltr",
    setLanguage,
    toggleLanguage: () => setLanguage((current) => (current === "ar" ? "en" : "ar")),
    t: (key) => translations[language][key] || translations.en[key] || key,
    formatDate: (value) => formatDate(value, language),
    formatDateTime: (value) => formatDateTime(value, language),
    formatWeekday: (value) => formatWeekday(value, language)
  }), [isArabic, language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }
  return context;
}

function formatDate(value, language) {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "";
  }
  const locale = language === "ar" ? "ar-SA-u-ca-gregory" : "en-US";
  return new Intl.DateTimeFormat(locale, {
    year: "numeric",
    month: language === "ar" ? "long" : "short",
    day: "numeric"
  }).format(date);
}

function formatDateTime(value, language) {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "";
  }
  const locale = language === "ar" ? "ar-SA-u-ca-gregory" : "en-US";
  return new Intl.DateTimeFormat(locale, {
    year: "numeric",
    month: language === "ar" ? "long" : "short",
    day: "numeric",
    hour: "numeric",
    minute: "2-digit",
    hour12: true
  }).format(date);
}

function formatWeekday(value, language) {
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return "";
  }
  const locale = language === "ar" ? "ar-SA-u-ca-gregory" : "en-US";
  return new Intl.DateTimeFormat(locale, { weekday: "short" }).format(date);
}
